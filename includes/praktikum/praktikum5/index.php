    <section id="layanan">
         <h2>Layanan Umum</h2>
         <p>Rumah Sakit Hewan Pendidikan Universitas Airlangga melakukan layanan-layanan, baik atas kehendak klien atau rujukan dokter hewan praktisi sebagai berikut:</p>
         <ul>
            <li><h3>Poliklinik</h3></li>
            <p>Poliklinik adalah layanan rawat jalan dimana pelayanan kesehatan hewan dilakukan tanpa pasien menginap. Poliklinik melayani tindakan observasi, diagnosis, pengobatan, rehabilitasi medik, serta pelayanan kesehatan lainnya seperti permintaan surat keterangan sehat. Tindakan observasi dan diagnosis, juga bisa diteguhkan dengan berbagai macam pemeriksaan yang bisa kami lakukan, misalnya pemeriksaan sitologi, dermatologi, hematologi, atau pemeriksaan radiologi, ultrasonografi, bahkan pemeriksaan elektrokardiografi. Bilamana diperlukan pemeriksaan-pemeriksaan lain yang diperlukan seperti pemeriksaan kultur bakteri, atau pemeriksaan jaringan/histopatologi, dan lain-lain kami bekerja sama dengan Fakultas Kedokteran Hewan Universitas Airlangga untuk membantu melakukan pemeriksaan-pemeriksaan tersebut. Selain itu kami mempunyai rapid test untuk pemeriksaan cepat, untuk meneguhkan diagnosa penyakit-penyakit berbahaya pada kucing seperti panleukopenia, calicivirus, rhinotracheitis, FIP, dan pada anjing seperti parvovirus, canine distemper.
                <p>Layanan kesehatan hewan di poliklinik yang kami lakukan antara lain:</p>
                <ol>
                    <li>Rawat jalan</li>
                    <li>Akupuntur</li>
                    <li>Kemoterapi</li>
                    <li>Fisioterapi</li>
                    <li>Mandi Terapi</li>
                </ol>

        <li><h3>Rawat Inap</h3></li>
        <p>Rawat inap dilakukan pada pasien-pasien yang berat atau parah dan membutuhkan perawatan intensif. Pasien akan diobservasi dan mendapat perawatan intensif dibawah pengawasan dokter dan paramedis yang handal. Sebelum rawat inap, klien wajib mengisi inform konsen yang artinya klien telah diberi penjelasan yang detail tentang kondisi penyakit pasien dan menyetujui rencana terapi yang akan dijalankan sepengetahuan klien. Klien juga diberitahu biaya yang dibebankan untuk semua layanan. RSHP menerima pembayaran tunai maupun kartu debit bank.</p>

        <li><h3>Bedah</h3></li>
        <ul>
            <li>Tindak Bedah Minor</li>
            <ul>
                <li>Jahit luka</li>
                <li>Kastrasi</li>
                <li>Othematoma</li>
                <li>Scaling - root planing</li>
                <li>Ekstraksi gigi</li>
            </ul>
            <li>Tindakan Bedah Mayor</li>
            <ul>
                <li>Gastrotomi; Entrotomi; Enterektomi; Salivary mucocele</li>
                <li>Ovariohisterektomi; Sectio caesar; Piometra</li>
                <li>Sistotomi; Urethrostomi</li>
                <li>Fraktur tulang</li>
                <li>Hernia diafragmatika</li>
                <li>Hernia perinealis</li>
                <li>Hernia inguinalis</li>
                <li>Eksisi tumor</li>
            </ul>
        </ul>      

        <li><h3>Pemeriksaan</h3></li>
